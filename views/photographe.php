        <h2>PARCOURS</h2>
        <p>J’ai débuté sérieusement la pratique de l’image à l’école de photographie contemporaine « Image Ouverte » de Nîmes en 2006, sous la direction de Serge Gal & David Brunel.</p>
        <p>J’y ai découvert les bases de tout ce que je sais aujourd’hui en matière de photo.
            Et j’y ai surtout « choppé le virus ».
            A partir de ce moment l’intérêt est devenu passion pour parfois frôler l’obsession.</p>
        <p>Les belles images... j’aime autant en regarder qu’en faire, les critiquer, les conserver précieusement au sein des piles de disques durs qui s’accumulent, revenir sur des travaux précédents avec un œil nouveau, toujours plus critique, bref…je suis loin d’être guéri.</p>
        <p>Je suis devenu officiellement professionnel à temps plein en 2011 et j’ai depuis photographié plus de 130 mariages.
            Tous différents, tous riches en émotions, je m’efforce de tous les couvrir du mieux que je peux pour fournir à mes clients les meilleures images possibles.</p>
        <p>VOUS PROPOSER UNE EXPÉRIENCE PHOTOGRAPHIQUE À MÊME DE RAVIVER CES BEAUX SOUVENIRS : C’EST LA MISSION QUE JE ME DONNE.</p>
    </div>

</section>

<section id="artistStyle">
    <div id="artistStyleTopContainer">
        <div id="artistStyleTopLeftContainer">
            <h2>STYLE & DEMARCHE</h2>
            <p>A la prise de vue, je tente d’être le plus discret possible, de n’interférer sur le déroulement naturel des événements que lorsque c’est indispensable.</p>
            <p>Vous l’aurez compris, plus de 90% de mon travail se concentre sur les images volées. Qu’il s’agisse de sourires complices, des larmes de vos grands-parents, d’amis qui ne se sont pas vus depuis une éternité...</p>
            <p>Et sinon ? Eh bien je tente de magnifier, de la plus belle façon qui soit, les photos de couple que l’on s’efforcera de faire, sans personne autour, dans des lieux toujours plus insolites et poétiques.
                Au moins autant accroc au monde végétal qu’à l’image, j’avoue avoir un gros faible pour les photos en pleine nature. Mesdames, prévoyez des baskets !</p>
            <p>Toutes les images que je rends font l’objet d’un soin particulier, individuel et minutieux en post traitement (rien que ça !).
                Je préfère d’ailleurs parler de traitement que de retouche.</p>
        </div>
        <div id="artistStyleImg">
            <img src="/public/assets/img/photo/artist-style-photographe-mariage-oise.webp" alt="photographe de mariage oise mariée se prepare">
        </div>
    </div>
    <div id="artistStyleBottomContainer">
        <p>J’aime les images contrastées, qu’il s’agisse de couleur ou de noir et blanc. Je les trouve plus fortes, plus « impactantes » ainsi.
            C’est un style que je peaufine à chaque instant</p>
        <p>Il peut parfois ne pas plaire mais c’est entièrement assumé et si c’était le cas, peut-être seriez-vous déjà partis !
            Je passe beaucoup de temps sur le post-traitement, qui me permet de modeler l’image brute comme je le souhaite. J’enferme donc à double tour les fichiers originaux dans mes disque durs. Je préfère ainsi ne vous rendre que des images réellement terminées .</p>
        <h3>La finalité, ce qui reste dans ma ligne de mire de notre premier rendez-vous à la dernière photo traitée,
            c’est de vous rendre un reportage à la hauteur de cette journée unique, à même de raviver vos souvenirs, même 30 ans plus tard.</h3>
        <p>Voilà pour ces « quelques lignes », j’en ai trop dit ou pas assez et j’ai surtout hâte de vous le montrer, ce travail !
            Il vous parlera bien mieux que moi.</p>
    </div>
